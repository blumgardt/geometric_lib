def area(a):
    '''
    Returns the area of a square
        Parameter:
            a (int): side of the square, decimal number
        Return value:
            area (int): decimal number, area of the square using the formula
    Example:
        print(area(r))

        Input: a = 4
        Output: 16
    '''

    return a * a


